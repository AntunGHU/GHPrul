# PRQ su nacini dostave na merge ili review promjena koje smo napravili u grani ili forku! One mogu ako su dobre biti prihvacene ili rejectane

# Posto su PRQ stvar GH, GH ima very nice Interface za rad sa PRQ-ima!

# kako smo o PRQ-ima  vec parlali sada gil vise paznje daje Automatically Merging-u. Kad vlasnik repoa odluci da su dobre predlozene izmjene trebamo odraditi Merge! Merging moze biti tricky thing i gill ce ga jos jednom gledati u nadolazecim demo-ima

# Ako su promjene na drugim fajlovima mozda GH sam skuzi i provede merge automaticaly, ako ne (doslo je do edita na istim djelovima; izvrsen je merge vise koji je izbrisao neke fajlove koje mi sad editamo) dolazi do tzv "Merge conflicta" koji treba biti razrijesen prije merganja!!!

# GH moze posluziti za merganje "Merging Inside GitHub". Alternativno, merganje mozemo vrsiti lokalno sa "Local Diff Tools" koji se konfiguriraju sa github: KDiff3, P4Merge, vimdiff3, BeyondCompare
