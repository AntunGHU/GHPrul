# Issues u GH nisu samo negativnosti kao bugs. Tu su i work-tasks, enchencements, id's, new features itd.

# Svaki repo ima issues tracker. Zatvaranje issua ne znaci gubitak infosa uz njega. Sve(open, closed) se prati, pise i pamti.

# Issues generate notificatios npr svaki komment na nas kreirani issue generira obavjest za nas. Povezani su i sa project board gdje se vrsi asigniranje pojedinih pojedinim clanovima tima.

# PRQsi su takodjer povezani s issuesima

# Kombinacija issuesa oznacava milestone!

# Issue can be created from a code in file or in PRQ

# Posto su jedno od centralnih obiljezja rada sa repoima GH je kreiran za rad s njima - kartica issues

# Na issuese mogu biti applayani "labels" (kao i na PRQse) koji pomazu u organizaciji i prioritizaciji issuesa. Po defu GH dolazi sa nekolicinom labelsa ali mozemo kreirati svoje i brisati defaultne

# builtin labelsi: bug, duplicate, enhancement, help wanted, question, wontfix, good first issue, invalid
