# Creating a project board (samo owner); Adding issues to our project board

# Creating: Tab Projects (i vise nema direktnog pristupa templatesima!) i "Add projects" sa mogucnosti biranja izmedju kreiranih ili kreiranje novog sa "Go to your profile to create a new project"

# Kad kliknemo za kreiranje novog prebacuje nas u profile view sa "Projects" i "Projects classic" gumbovima lijevo i "New project" gumbom desno. Opcije i kombinacije su dosta nove a ono sto gill objasnjava sad se nalazi kao "classic project"

# Classic Project: ime, opis, project template (bira empty) i klik na "Create project" --> stvara se sa napomenom da nema kolona ni karti i gumbom "Add column".

# Klik na "Add column": Not started; Automation (todo i sto ce se automatski pridodavati na kolumne, gil izabrao: new issues i new PRQ); In progress; Done items

# Kako bi stvorio cardse ide u issuese: klika na desno u "Projects" i pojedini issue pridruzuje pojedinom projectu! No pridruzivanje daje samo nove projekte, pitanje kako iz klasicnog kreirati novi? Gill nakon pridruzivanja otvara projekt i pokazuje kako je automatizacija odradila i svi newly created issues su se nasli na kartici Nezaopoceto! Klik na pojedini daje bitne tocke svakog issuea.

# Pokazuje kako na samoj kolumni moze kreirati note i posle ju upgradati u issue! Kad smo isue rjesili samo ga drag-amo u sljedecu kolumnu!

# Issuesi se mogu filtrirati tako da board pokazuje samo njih!

# Sa menia moguce je zatvoriti project. Kasnije se on moze i reopenati!
